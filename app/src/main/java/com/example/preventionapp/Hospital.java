package com.example.preventionapp;

public class Hospital {

    private String name;
    private String addr;
    private String tel;
    private double lat;
    private double lon;

    public Hospital(String name, String addr, String tel, double lat, double lon) {
        this.name = name;
        this.addr = addr;
        this.tel = tel;
        this.lat = lat;
        this.lon = lon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}
